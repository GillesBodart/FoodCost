    private void jButtonAIActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAIActionPerformed
        AjoutComposant aI = new AjoutComposant(null, true);
        aI.setVisible(true);
        if (aI.getExit() == 0) {
            lastComp = aI.getComp();
            lastPanelComp = new JPanelComp(lastComp);
            jListeRecette.addElem(lastPanelComp);
            prixTmp += lastComp.getPrix();
            lstComp.add(lastComp);
            jLabelPrix.setText("" + CommonTool.round(prixTmp, 4));
            if (!lstComp.isEmpty()) {
                jButtonSupI.setEnabled(true);
            }
        }
    }//GEN-LAST:event_jButtonAIActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            commande = new CommandeDto(0, jTextNom.getText(), new ListeRecetteDto(0, 0, lstComp), prixTmp);
            AdminFacade.AjoutCommande(commande);
            this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,
                    "Fais attention a ce que tu fais! tu vas tout casser !!" + ex.getMessage(), "Error Massage",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButtonSupIActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSupIActionPerformed

        if (!lstComp.isEmpty()) {
            jListeRecette.removeLastPanel();
            prixTmp -= lastComp.getPrix();
            lstComp.remove(lastComp);
            jLabelPrix.setText("" + CommonTool.round(prixTmp, 4));
            if (lstComp.isEmpty()) {
                jButtonSupI.setEnabled(false);
            }
        }
    }//GEN-LAST:event_jButtonSupIActionPerformed

